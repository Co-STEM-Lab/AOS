def load_atoms() -> list[dict]:
    """加载全部原子（跳过 example- 文件）。"""
    atoms = []
    if not ATOMS_DIR.is_dir():
        return atoms

    for f in sorted(ATOMS_DIR.rglob("*.md")):
        # 跳过示例文件
        if f.parent.stem == "example" or f.name.startswith("example-"):
            continue
        # 跳过 scripts 目录下的文件
        if f.parent.stem == "scripts":
            continue
        fm = parse_front_matter(str(f))
        if not fm:
            continue
        body = parse_body(str(f))
        # 提取纯文本预览（去 Markdown 符号，去标题行）
        body_preview = body.strip()
        body_preview = re.sub(r'^#.*$', '', body_preview, flags=re.MULTILINE)
        body_preview = re.sub(r'[#*_`>\[\]]', '', body_preview)
        body_preview = re.sub(r'\n{3,}', '\n\n', body_preview)
        body_preview = body_preview.strip()[:200]
        atom = {
            "id": fm.get("id", f.stem),
            "title": fm.get("title", f.stem),
            "type": fm.get("type", "unknown"),
            "tags": fm.get("tags", []),
            "project": fm.get("project", "uncategorized"),
            "status": fm.get("status", "draft"),
            "source": fm.get("source", ""),
            "created": fm.get("created", ""),
            "body": body,
            "body_preview": body_preview,
            "body_html": md_to_html(body),
            "script": fm.get("script", ""),
            "script_deps": fm.get("script_deps", []),
            "script_input": fm.get("script_input", ""),
            "script_input_desc": fm.get("script_input_desc", ""),
            "script_args": fm.get("script_args", ""),
        }
        atoms.append(atom)
    return atoms


