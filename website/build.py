def load_skill_tree() -> dict:
    """解析技能树，返回 { 分类: [技能列表] }。"""
    categories = {}
    if not SKILL_TREE_PATH.exists():
        return categories

    content = SKILL_TREE_PATH.read_text(encoding="utf-8")
    lines = content.split("\n")

    current_category = "未分类"
    # 解析 ## 标题作为分类
    for line in lines:
        m = re.match(r"^##\s+(.+)", line)
        if m:
            current_category = m.group(1).strip()

    # 解析表格行
    current_category = "未分类"
    skill_rows = []
    in_table = False
    for line in lines:
        cm = re.match(r"^##\s+(.+)", line)
        if cm:
            if skill_rows:
                categories[current_category] = skill_rows
            current_category = cm.group(1).strip()
            skill_rows = []
            in_table = False
            continue

        # 检测表格行: | 技能 | 评级 | 证据 |
        if re.match(r"^\|\s*[^|]+\|\s*[^|]+\|\s*[^|]+\s*\|", line):
            cells = [c.strip() for c in line.split("|")[1:-1]]
            if len(cells) >= 3 and cells[0] not in ("技能", "------", ""):
                skill_rows.append({
                    "name": cells[0],
                    "level": cells[1] if cells[1] != "--" else "",
                    "evidence": cells[2] if len(cells) > 2 else "",
                })

    if skill_rows:
        categories[current_category] = skill_rows

    return categories


def load_matrix() -> dict | None:
    """解析 matrix.md，返回矩阵结构。"""
    if not MATRIX_PATH.exists():
        return None

    content = MATRIX_PATH.read_text(encoding="utf-8")

    # 去掉示例区域（## 使用示例 到下一个 ## 或文件尾）
    example_start = content.find("## 使用示例")
    if example_start > 0:
        next_section = content.find("\n## ", example_start + 1)
        if next_section > 0:
            content = content[:example_start] + content[next_section:]
        else:
            content = content[:example_start]

    # 找到第一个表格
    lines = content.split("\n")
    table_start = None
    for i, line in enumerate(lines):
        if line.startswith("|") and "-" in line:
            # 这是表头分隔行
            header_line = lines[i - 1] if i > 0 else ""
            if header_line.startswith("|"):
                table_start = i - 1
                break

    if table_start is None:
        return None

    # 提取表格行
    header_parts = [c.strip() for c in lines[table_start].split("|")[1:-1]]
    problems = header_parts[1:] if len(header_parts) > 1 else []
    domains = []
    rows = []

    for i in range(table_start + 2, len(lines)):
        line = lines[i]
        if not line.startswith("|"):
            break
        cells = [c.strip() for c in line.split("|")[1:-1]]
        if len(cells) < 2:
            continue
        domain = cells[0]
        if domain and not domain.startswith("<!--"):
            domains.append(domain)
            row = {"domain": domain, "cells": []}
            for j, cell in enumerate(cells[1:]):
                # 提取项目链接和状态
                proj_match = re.search(r"\[([^\]]+)\]\(([^)]+)\)", cell)
                status_match = re.search(r"([✅🔬✍️📤💡])", cell)
                row["cells"].append({
                    "raw": cell,
                    "project_id": proj_match.group(1) if proj_match else "",
                    "project_url": proj_match.group(2) if proj_match else "",
                    "status": status_match.group(1) if status_match else "",
                    "empty": cell.strip() in ("", "—", "-"),
                })
            rows.append(row)

    return {"problems": problems, "domains": domains, "rows": rows}


def load_projects() -> dict:
    """加载全部项目，按桶分类。"""
    projects = {"ideas": [], "active": [], "completed": []}
    if not PROJECTS_DIR.is_dir():
        return projects

    bucket_map = {"ideas": "ideas", "active": "active", "completed": "completed"}
    for bucket_key, bucket_name in bucket_map.items():
        bucket_dir = PROJECTS_DIR / bucket_name
        if not bucket_dir.is_dir():
            continue
        for proj_dir in sorted(bucket_dir.iterdir()):
            if not proj_dir.is_dir():
                continue
            idx = proj_dir / "index.md"
            if not idx.exists():
                continue
            fm = parse_front_matter(str(idx))
            if not fm:
                continue
            body = parse_body(str(idx))

            # 扫描报告 HTML 和数据目录
            reports = sorted([f.name for f in proj_dir.glob("report-*.html")])
            has_data_dir = (proj_dir / "data").is_dir()

            proj = {
                "id": fm.get("id", proj_dir.name),
                "title": fm.get("title", proj_dir.name),
                "status": fm.get("status", "idea"),
                "domain": fm.get("domain", ""),
                "problem": fm.get("problem", ""),
                "priority": fm.get("priority", "medium"),
                "created": fm.get("created", ""),
                "updated": fm.get("updated", ""),
                "description": body,
                "description_html": md_to_html(body),
                "url": f"/projects/{proj_dir.name}/",
                "dir_name": proj_dir.name,
                "reports": reports,
                "has_data_dir": has_data_dir,
            }
            projects[bucket_key].append(proj)

    return projects


