def build_site(lang: str, config: dict, atoms: list, skill_tree: dict,
               matrix: dict | None, projects: dict, vocab: dict,
               env: Environment) -> int:
    """构建指定语言版本的全站。返回生成的文件数。"""
    t = T.get(lang, T["zh"])
    pages_created = 0
    is_primary = lang == config.get("site", {}).get("lang_primary", "zh")
    prefix = "" if is_primary else f"{lang}/"

    mkdir = lambda p: (PUBLIC_DIR / p).mkdir(parents=True, exist_ok=True)

    # 基础上下文
    ctx = {
        "lang": lang,
        "lang_other": t["switch_label"],
        "lang_other_url": t["switch_url"],
        "config": config,
        "t": t,
        "vocab": vocab,
        "current_year": date.today().year,
        "is_primary": is_primary,
        "prefix": prefix,
    }

    # ── 首页 ──
    mkdir(prefix)
    index_ctx = {**ctx,
        "page_id": "index",
        "atoms": atoms[:config.get("homepage", {}).get("show_latest_atoms", 5)],
        "active_projects": projects.get("active", [])[:config.get("homepage", {}).get("show_featured_projects", 3)],
        "resolve_atom_type_name": resolve_atom_type_name,
    }
    _write_page("index.html", index_ctx, env, PUBLIC_DIR / prefix / "index.html")
    pages_created += 1

    # ── 研究领域 ──
    mkdir(prefix + "research")
    research_ctx = {**ctx, "page_id": "research", "matrix": matrix,
        "resolve_project_status_name": resolve_project_status_name}
    _write_page("research.html", research_ctx, env, PUBLIC_DIR / prefix / "research" / "index.html")
    pages_created += 1

    # ── 学术产出 ──
    mkdir(prefix + "outputs")
    pubs_data = load_publications()
    sw_data = load_software()
    research_atoms = [a for a in atoms if a["type"] in ("result", "method")]
    outputs_ctx = {**ctx, "page_id": "outputs",
        "publications": pubs_data,
        "software": sw_data,
        "research_atoms": research_atoms,
        "research_types": sorted(set(a["type"] for a in research_atoms)),
        "pub_types": sorted(set(p["type"] for p in pubs_data)),
        "resolve_atom_type_name": resolve_atom_type_name}
    _write_page("outputs.html", outputs_ctx, env, PUBLIC_DIR / prefix / "outputs" / "index.html")
    pages_created += 1

    # ── 项目 ──
    mkdir(prefix + "projects")
    projs_ctx = {**ctx, "page_id": "projects", "projects": projects,
        "resolve_project_status_name": resolve_project_status_name}
    _write_page("projects.html", projs_ctx, env, PUBLIC_DIR / prefix / "projects" / "index.html")
    pages_created += 1

    # 单个项目详情页
    for bucket_key in ("active", "completed", "ideas"):
        for proj in projects.get(bucket_key, []):
            proj_dir_name = proj["id"]
            mkdir(prefix + f"projects/{proj_dir_name}")
            proj_ctx = {**ctx, "page_id": "project", "project": proj,
                "bucket_key": bucket_key,
                "project_atoms": [a for a in atoms if a["project"] == proj["id"]],
                "resolve_atom_type_name": resolve_atom_type_name,
                "resolve_project_status_name": resolve_project_status_name}
            _write_page("project-single.html", proj_ctx, env,
                        PUBLIC_DIR / prefix / "projects" / proj_dir_name / "index.html")
            pages_created += 1

            # 复制项目报告 HTML 和 data/ 图片到网站输出
            _copy_project_assets(bucket_key, proj, prefix)

    # ── 能力 ──
    mkdir(prefix + "skills")
    skills_ctx = {**ctx, "page_id": "skills", "skill_tree": skill_tree,
        "skill_level_int": skill_level_int}
    _write_page("skills.html", skills_ctx, env, PUBLIC_DIR / prefix / "skills" / "index.html")
    pages_created += 1

    # ── 知识库 ──
    mkdir(prefix + "atoms")
    all_tags = sorted(set(tag for a in atoms for tag in a.get("tags", [])))
    all_types = sorted(set(a["type"] for a in atoms))
    atoms_ctx = {**ctx, "page_id": "atoms", "atoms": atoms,
        "all_tags": all_tags, "all_types": all_types,
        "resolve_atom_type_name": resolve_atom_type_name}
    _write_page("atoms.html", atoms_ctx, env, PUBLIC_DIR / prefix / "atoms" / "index.html")
    pages_created += 1

    # 单个原子详情页
    for atom in atoms:
        aid = atom["id"]
        mkdir(prefix + f"atoms/{aid}")
        atom_ctx = {**ctx, "page_id": "atom", "atom": atom,
            "resolve_atom_type_name": resolve_atom_type_name}
        _write_page("atom-single.html", atom_ctx, env,
                    PUBLIC_DIR / prefix / "atoms" / aid / "index.html")
        pages_created += 1

    return pages_created


def _copy_project_assets(bucket: str, proj: dict, prefix: str):
    """复制项目的报告 HTML 和 data/ 目录到网站输出。"""
    proj_src = PROJECTS_DIR / bucket / proj["dir_name"]
    proj_dst = PUBLIC_DIR / prefix / "projects" / proj["dir_name"]

    # 复制 report-*.html
    for rpt in proj.get("reports", []):
        rpt_src = proj_src / rpt
        if rpt_src.exists():
            shutil.copy2(str(rpt_src), str(proj_dst / rpt))
            print(f"     📄 报告: {proj['dir_name']}/{rpt}")

    # 复制 data/ 目录（图片等）
    data_src = proj_src / "data"
    if data_src.is_dir():
        data_dst = proj_dst / "data"
        shutil.copytree(str(data_src), str(data_dst), dirs_exist_ok=True)
        n_files = len(list(data_dst.rglob("*")))
        print(f"     🖼️  图片: {proj['dir_name']}/data/ ({n_files} 个文件)")


def _write_page(template_name: str, context: dict, env: Environment, output_path: Path):
    """渲染模板并写入文件。"""
    try:
        template = env.get_template(template_name)
        html = template.render(**context)
        output_path.parent.mkdir(parents=True, exist_ok=True)
        output_path.write_text(html, encoding="utf-8")
    except Exception as e:
        print(f"[ERROR] 渲染 {template_name} → {output_path}: {e}")
        import traceback
        traceback.print_exc()


