def _get_watch_paths() -> list[Path]:
    """返回需要监听的文件列表。"""
    paths = []
    paths.append(CONFIG_PATH)
    paths.append(MATRIX_PATH)
    paths.append(SKILL_TREE_PATH)
    paths.append(VOCAB_PATH)
    if TEMPLATES_DIR.is_dir():
        paths.extend(sorted(TEMPLATES_DIR.rglob("*.html")))
    if STATIC_DIR.is_dir():
        paths.extend(sorted(STATIC_DIR.rglob("*")))
    if ATOMS_DIR.is_dir():
        paths.extend(sorted(ATOMS_DIR.rglob("*.md")))
    if PROJECTS_DIR.is_dir():
        for p in PROJECTS_DIR.rglob("*.md"):
            if "index.md" in str(p):
                paths.append(p)
    return [p for p in paths if p.exists()]


def _get_mtime_map(paths: list[Path]) -> dict[str, float]:
    """返回 {str(path): mtime} 映射。"""
    return {str(p): p.stat().st_mtime for p in paths}


def _start_watch(config: dict, vocab: dict, env: Environment):
    """文件监听循环，检测到变化后自动重建。"""
    print("\n  👀 监听模式已启动 (Ctrl+C 停止)")
    print("     监听范围: config.yml / 模板 / CSS / 原子 / 项目 / 矩阵 / 技能树\n")

    watch_paths = _get_watch_paths()
    mtime_map = _get_mtime_map(watch_paths)
    poll_interval = 1.5  # 秒

    import time as _time
    try:
        while True:
            _time.sleep(poll_interval)
            changed = []
            for p in watch_paths:
                if not p.exists():
                    continue
                current = p.stat().st_mtime
                if abs(current - mtime_map.get(str(p), 0)) > 0.01:
                    changed.append(p)
                    mtime_map[str(p)] = current

            if changed:
                print(f"\n  🔄 检测到 {len(changed)} 个文件变化，重新构建...")
                for c in changed[:5]:
                    print(f"     {c.relative_to(ROOT)}")
                if len(changed) > 5:
                    print(f"     ...及其他 {len(changed) - 5} 个")

                # 更新路径列表（可能有新增文件）
                watch_paths = _get_watch_paths()
                mtime_map = _get_mtime_map(watch_paths)

                # 重建
                _do_rebuild(config, vocab, env)
    except KeyboardInterrupt:
        print("\n  👋 监听已停止")


